# 3. Correlation vs Causation
# The phrase "correlation does not imply causation" means:
# Just because two variables show a relationship does not mean one causes the other.

# Example:
# Suppose there is a positive correlation between the number of firefighters at a fire
# and the amount of damage caused by the fire.
# It may seem like firefighters cause more damage, but the actual reason could be:
# - The severity of the fire.
# - Bigger fires need more firefighters and naturally cause more damage.
# So the third variable (fire severity) explains both.

# Correlation is useful to detect relationships between variables.
# But to prove one variable causes another, we need controlled experiments or strong observational studies.
