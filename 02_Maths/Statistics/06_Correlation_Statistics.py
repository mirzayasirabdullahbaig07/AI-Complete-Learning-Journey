# 1. What problem does Correlation solve?
# Correlation helps us understand the degree and direction of the relationship between two variables.
# It answers: Are the variables moving together? If so, how strongly and in which direction?

# Types of correlation:
# Strong positive correlation     → variables move together tightly in the same direction
# Weak positive correlation       → variables loosely move together in the same direction
# Strong negative correlation     → variables move tightly in opposite directions
# Weak negative correlation       → variables loosely move in opposite directions
# Moderate negative correlation   → variables moderately move in opposite directions
# No correlation                  → no linear relationship between the variables

# Can we quantify this weak and strong relationship? Yes, using a correlation coefficient.

# 2. What is correlation?
# Correlation refers to a statistical relationship between two or more variables.
# Specifically, it measures the degree to which two variables are related and how they tend to change together.

# Correlation is measured using a statistical tool called the correlation coefficient,
# which ranges from -1 to 1:
# - A correlation coefficient of +1 indicates a perfect positive correlation.
# - A correlation coefficient of -1 indicates a perfect negative correlation.
# - A correlation coefficient of 0 indicates no linear relationship.

