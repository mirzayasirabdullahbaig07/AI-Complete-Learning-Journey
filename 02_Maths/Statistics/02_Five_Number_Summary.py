# What is the 5-number summary in statistics?
# The five-number summary is a descriptive statistic that provides a concise overview of a dataset. 
# It consists of five key values that divide the dataset into four equal parts, also known as quartiles.

# 1 - Minimum value: The smallest value in the dataset
# 2 - First Quartile (Q1): The value that separates the lowest 25% of the data from the rest
# 3 - Median (Q2): The value that divides the dataset into two equal halves (50% lower, 50% higher)
# 4 - Third Quartile (Q3): The value that separates the lowest 75% of the data from the highest 25%
# 5 - Maximum value: The largest value in the dataset

# The five-number summary is often represented visually using a box plot, 
# which displays the range, the median, and the quartiles of the dataset.

# This summary is a quick and effective way to understand the central tendency, variability, 
# and distribution of a dataset.

# What is Interquartile Range (IQR)?
# The interquartile range is a measure of variability based on the five-number summary.
# Specifically, the IQR is defined as the difference between the third quartile (Q3) and the first quartile (Q1):
# IQR = Q3 - Q1
